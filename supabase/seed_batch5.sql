 '## 设计思维五步法

设计思维（Design Thinking）是一种以人为中心的创新方法论。它由斯坦福大学 d.school 系统化提出，被 IDEO、苹果、谷歌等公司广泛应用。五步法不是线性流程，而是一个可以反复迭代的循环。

### 第一步：共情（Empathize）

放下自己的假设，走进用户的世界。

- 观察用户在真实场景中的行为（不是他们说的，而是他们做的）
- 进行深度访谈，问「为什么」至少 5 次
- 体验用户的一天，感受他们的痛点和喜悦

关键心态：你不是用户。你的偏好不等于用户的需求。

### 第二步：定义（Define）

把共情阶段收集到的大量信息，提炼成一个清晰的设计挑战。

- 好的问题定义格式：「[用户] 需要 [需求]，因为 [洞察]」
- 例：「经常加班的年轻妈妈需要一种在 10 分钟内完成的放松方式，因为她们只有在孩子睡着后的碎片时间里才能照顾自己」

关键心态：定义对了问题，解决方案往往会自然浮现。

### 第三步：发散（Ideate）

这是最自由的阶段——尽可能多地产生想法，不评判、不否定。

- 头脑风暴：追求数量，鼓励疯狂想法，在别人的想法上叠加
- 画草图：不追求精美，能传达想法即可
- 反向思考：想想「怎么做会让体验更糟糕」，然后反过来做

关键心态：创新不是等灵感，而是系统性地产出大量选项。

